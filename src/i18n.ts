export const languageOptions = [
  { value: "system", labelKey: "language.system" },
  { value: "en", labelKey: "language.english" },
  { value: "zh-CN", labelKey: "language.chineseSimplified" },
] as const;

export type LanguagePreference = (typeof languageOptions)[number]["value"];
export type SupportedLocale = "en" | "zh-CN";

export type I18n = {
  locale: SupportedLocale;
  languagePreference: LanguagePreference;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
};

const dictionaries: Record<SupportedLocale, Record<MessageKey, string>> = {
  en,
  "zh-CN": zh,
};

export function createI18n(preference: string | undefined): I18n {
  const languagePreference = normalizePreference(preference);
  const locale = resolveLocale(languagePreference);

  return {
    locale,
    languagePreference,
    t: (key, values) => interpolate(dictionaries[locale][key] ?? en[key], values),
  };
}

function normalizePreference(preference: string | undefined): LanguagePreference {
  if (preference === "en" || preference === "zh-CN" || preference === "system") {
    return preference;
  }

  return "system";
}

function resolveLocale(preference: LanguagePreference): SupportedLocale {
  if (preference !== "system") {
    return preference;
  }

  const systemLanguages =
    typeof navigator === "undefined"
      ? []
      : [...navigator.languages, navigator.language].filter(Boolean);

  for (const language of systemLanguages) {
    const normalized = language.toLowerCase();
    if (normalized.startsWith("zh")) {
      return "zh-CN";
    }
    if (normalized.startsWith("en")) {
      return "en";
    }
  }

  return "en";
}

function interpolate(
  message: string,
  values: Record<string, string | number> | undefined,
) {
  if (!values) {
    return message;
  }

  return Object.entries(values).reduce(
    (result, [key, value]) => result.split(`{${key}}`).join(String(value)),
    message,
  );
}
